package com.app.test;

public class IfElseIf {

	public static void main(String[] args) {
		
		if( 100 < 50 ) {
			
			System.out.println("in if");
			
		} else if( 200 > 200 ) {
			
			System.out.println("in else if 1");
			
		} else if( 200 >= 200 ) {
			
			System.out.println("in else if 2");
			
		} else {
			
			System.out.println("in else");
			
		}
		
	}
}
